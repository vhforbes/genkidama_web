import type { NextPage } from 'next';
import Head from 'next/head';
import { useRouter } from 'next/router';
import React, { useEffect } from 'react';

import PostsContainerComponent from '../components/posts/postsContainer.component';
import TradeOperationCard from '../components/tradeOperations/tradeOperationCard';
import TradeOperationsContainer from '../components/tradeOperations/tradeOperationsContainer';
import { useAuth } from '../hooks/auth';
import { useSubscription } from '../hooks/subscription';

const Home: NextPage = () => {
  const { user } = useAuth();
  const router = useRouter();
  const { checkSub, subscription } = useSubscription();

  if (!user) {
    router.push('/sign-in');
    return null;
  }

  useEffect(() => {
    checkSub();
  }, []);

  if (subscription.status === 'ACTIVE')
    return (
      <div>
        <Head>
          <title>Create Next App</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>

        <main className="h-full">
          {/* <Loader /> */}
          <div className="flex flex-col-reverse 2xl:flex-row p-10">
            <div className="w-auto md:w-4/5 md:mr-16">
              <PostsContainerComponent />
            </div>
            <div>
              <TradeOperationsContainer />
            </div>
          </div>
        </main>
      </div>
    );

  return null;
};

export default Home;
